#include<stdio.h>
int main()
{
	int a,b,c;
	scanf("%d %d",&a,&b);
	printf("before swapping",a,b);
	printf("%d %d\n",a,b);
	c=b;
	b=a;
	printf("after swapping %d %d",c,b);
	return 0;                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                       
}